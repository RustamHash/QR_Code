echo "2. –ó–∞–ø—É—Å—Ç–∏—Ç–µ –±–æ—Ç–∞: sudo systemctl start qr-code-bot"
echo "3. –ü—Ä–æ–≤–µ—Ä—å—Ç–µ —Å—Ç–∞—Ç—É—Å: sudo systemctl status qr-code-bot"
echo "4. –ü—Ä–æ—Å–º–æ—Ç—Ä –ª–æ–≥–æ–≤: sudo journalctl -u qr-code-bot -f"

