# –°–æ–∑–¥–∞–Ω–∏–µ .env —Ñ–∞–π–ª–∞ (–µ—Å–ª–∏ –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç)
if [ ! -f "$PROJECT_DIR/.env" ]; then
    echo -e "${YELLOW}‚öôÔ∏è –°–æ–∑–¥–∞–Ω–∏–µ —Ñ–∞–π–ª–∞ .env –∏–∑ –ø—Ä–∏–º–µ—Ä–∞...${NC}"
    cp .env.example .env
    echo -e "${RED}‚ö†Ô∏è  –í–ù–ò–ú–ê–ù–ò–ï: –û—Ç—Ä–µ–¥–∞–∫—Ç–∏—Ä—É–π—Ç–µ —Ñ–∞–π–ª .env –∏ —É–∫–∞–∂–∏—Ç–µ TELEGRAM_BOT_TOKEN!${NC}"
    echo -e "${YELLOW}   nano $PROJECT_DIR/.env${NC}"
